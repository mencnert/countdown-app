import {formatDurationV2} from "./utils"

test('format duration', () => {
  expect(formatDurationV2(0)).toBe("+ 00d 00h 00m 00s")
  expect(formatDurationV2(5)).toBe("+ 00d 00h 00m 05s")
  expect(formatDurationV2(55)).toBe("+ 00d 00h 00m 55s")
  expect(formatDurationV2(65)).toBe("+ 00d 00h 01m 05s")
  expect(formatDurationV2(105)).toBe("+ 00d 00h 01m 45s")
  expect(formatDurationV2(305)).toBe("+ 00d 00h 05m 05s")
  expect(formatDurationV2(355)).toBe("+ 00d 00h 05m 55s")
  expect(formatDurationV2(3300)).toBe("+ 00d 00h 55m 00s")
  expect(formatDurationV2(3355)).toBe("+ 00d 00h 55m 55s")
  expect(formatDurationV2(3600)).toBe("+ 00d 01h 00m 00s")
  expect(formatDurationV2(3655)).toBe("+ 00d 01h 00m 55s")
  expect(formatDurationV2(3900)).toBe("+ 00d 01h 05m 00s")
  expect(formatDurationV2(3955)).toBe("+ 00d 01h 05m 55s")
  expect(formatDurationV2(36000)).toBe("+ 00d 10h 00m 00s")
  expect(formatDurationV2(36300)).toBe("+ 00d 10h 05m 00s")
  expect(formatDurationV2(36355)).toBe("+ 00d 10h 05m 55s")
  expect(formatDurationV2(86400)).toBe("+ 01d 00h 00m 00s")
  expect(formatDurationV2(86700)).toBe("+ 01d 00h 05m 00s")
  expect(formatDurationV2(86755)).toBe("+ 01d 00h 05m 55s")
  expect(formatDurationV2(90355)).toBe("+ 01d 01h 05m 55s")
  expect(formatDurationV2(864000)).toBe("+ 10d 00h 00m 00s")

  expect(formatDurationV2(-5)).toBe("- 00d 00h 00m 05s")
  expect(formatDurationV2(-55)).toBe("- 00d 00h 00m 55s")
  expect(formatDurationV2(-65)).toBe("- 00d 00h 01m 05s")
  expect(formatDurationV2(-105)).toBe("- 00d 00h 01m 45s")
  expect(formatDurationV2(-305)).toBe("- 00d 00h 05m 05s")
  expect(formatDurationV2(-355)).toBe("- 00d 00h 05m 55s")
  expect(formatDurationV2(-3300)).toBe("- 00d 00h 55m 00s")
  expect(formatDurationV2(-3355)).toBe("- 00d 00h 55m 55s")
  expect(formatDurationV2(-3600)).toBe("- 00d 01h 00m 00s")
  expect(formatDurationV2(-3655)).toBe("- 00d 01h 00m 55s")
  expect(formatDurationV2(-3900)).toBe("- 00d 01h 05m 00s")
  expect(formatDurationV2(-3955)).toBe("- 00d 01h 05m 55s")
  expect(formatDurationV2(-36000)).toBe("- 00d 10h 00m 00s")
  expect(formatDurationV2(-36300)).toBe("- 00d 10h 05m 00s")
  expect(formatDurationV2(-36355)).toBe("- 00d 10h 05m 55s")
  expect(formatDurationV2(-86400)).toBe("- 01d 00h 00m 00s")
  expect(formatDurationV2(-86700)).toBe("- 01d 00h 05m 00s")
  expect(formatDurationV2(-86755)).toBe("- 01d 00h 05m 55s")
  expect(formatDurationV2(-90355)).toBe("- 01d 01h 05m 55s")
  expect(formatDurationV2(-864000)).toBe("- 10d 00h 00m 00s")
});

export {}